complex1 = int(input("Введіть кількість артилерійських комплексів у першому підрозділі: "))
complex2 = int(input("Введіть кількість артилерійських комплексів у другому підрозділі: "))
complex3 = int(input("Введіть кількість артилерійських комплексів у третьому підрозділі: "))

average = (complex1 + complex2 + complex3) / 3

average = round(average, 2)

print("Середня кількість артилерійських комплексів у підрозділах:", average)
