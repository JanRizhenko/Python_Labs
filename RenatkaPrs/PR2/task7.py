import math

a = int(input("Введіть перше число: "))
b = int(input("Введіть друге число: "))

print("1. Перше число кратне другому:", a % b == 0 if b != 0 else False)

print("2. Сума двох чисел парна:", (a + b) % 2 == 0)

print("3. Друге число більше за перше:", b > a)

print("4. Перше число менше за друге:", a < b)

print("5. Сума більша за 100:", a + b > 100)

print("6. Різниця між числами менша за 10:", abs(a - b) < 10)

print("7. Перше число додатне, друге — від’ємне:", a > 0 and b < 0)

print("8. Числа взаємно прості (НСД = 1):", math.gcd(a, b) == 1)

print("9. Перше число є дільником другого:", b % a == 0 if a != 0 else False)
