text = input("Введіть рядок з потенційною електронною адресою: ")

if '@' in text and '.' in text:
    last_dot = text.rfind('.')
    if last_dot < len(text) - 2:
        print("Рядок містить коректну електронну адресу.")
    else:
        print("Неправильна адреса: занадто мало символів після останньої крапки.")
else:
    print("Неправильна адреса: відсутній символ '@' або крапка.")
