text = input("Введіть рядок: ")

if text.isdigit():
    number = int(text)
    if number % 4 == 0:
        print("Цей текст — десятковий запис числа, кратного 4.")
    else:
        print("Цей текст — десятковий запис числа, але не кратного 4.")
else:
    print("Цей текст не є десятковим записом числа.")
